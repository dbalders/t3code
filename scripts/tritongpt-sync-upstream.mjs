function handlePublishOptions({ worktree, syncBranch, report, canAutoMerge }) {
  const labels = labelsForReport({ report, canAutoMerge });
  const reportFiles = writeReportFiles({ worktree, report, labels });

  if (options.createPr && !options.push) {
    throw new Error("Refusing --create-pr without --push; the sync branch must exist on origin.");
  }

  if (options.push) {
    runGit(["push", config.originRemote, `${syncBranch}:${syncBranch}`], { cwd: worktree });
    console.log(`Pushed ${syncBranch} to ${config.originRemote}.`);
  }

  let pr = null;
  if (options.createPr) {
    pr = createPullRequest({
      cwd: worktree,
      syncBranch,
      prBodyPath: reportFiles.prBodyPath,
      report,
      labels,
    });
  }

  const shouldMergePr = options.autoMergePr || (options.autoMerge && options.createPr);
  if (shouldMergePr) {
    if (!canAutoMerge) {
      console.log("Skipping PR merge because the report is not auto-merge-ready.");
      return;
    }

    if (!pr) {
      console.error("Refusing --auto-merge-pr without --create-pr.");
      process.exitCode = 3;
      return;
    }

    mergePullRequest({ cwd: worktree, syncBranch, report, pr });
    return;
  }

  if (options.autoMerge) {
    if (!canAutoMerge) {
      console.error("Refusing --auto-merge because the report is not auto-merge-ready.");
      process.exitCode = 3;
      return;
    }

    if (!options.push) {
      console.error("Refusing --auto-merge without --push.");
      process.exitCode = 3;
      return;
    }

    console.warn(
      "Direct branch update via --auto-merge is deprecated; prefer --create-pr --auto-merge-pr.",
    );
    runGit(["push", config.originRemote, `HEAD:refs/heads/${config.brandBranch}`], {
      cwd: worktree,
    });
    console.log(`Updated ${config.originRemote}/${config.brandBranch}.`);
  }
}

function writeReportFiles({ worktree, report, labels }) {
  const reportPath = join(worktree, "tritongpt-sync-report.json");
  const prBodyPath = join(worktree, "tritongpt-sync-pr.md");
  writeFileSync(reportPath, `${JSON.stringify(report, null, 2)}\n`);
  writeFileSync(prBodyPath, buildPullRequestBody({ report, labels }), "utf8");
  return { reportPath, prBodyPath };
}

function buildPullRequestBody({ report, labels }) {
  const review = report.review || {};
  const risks = Array.isArray(review.risks) && review.risks.length > 0 ? review.risks : [];
  const checks = report.checks
    ? report.checks.ok
      ? report.checks.skipped
        ? "skipped"
        : "passed"
      : "failed"
    : "not run";
  const conflictFiles = report.remainingConflicts?.length
    ? report.remainingConflicts
    : report.conflicts || [];
  const upstreamCommits = firstLines(report.changeSummary?.upstreamCommits || "", 12);
  const diffStat = firstLines(
    report.changeSummary?.mergedDiffStat || report.changeSummary?.upstreamDiffStat || "",
    40,
  );
  const nameStatus = firstLines(
    report.changeSummary?.mergedNameStatus || report.changeSummary?.upstreamNameStatus || "",
    40,
  );
  const action =
    report.status === "auto-merge-ready"
      ? "Ready to merge when the workflow was run with PR auto-merge enabled."
      : "Needs human review before merging.";
  return [
    "## Summary",
    "",
    `- Status: \`${report.status}\``,
    `- Reason: \`${report.reason}\``,
    `- Action: ${action}`,
    `- Upstream: \`${report.upstreamRef}\` @ \`${shortSha(report.upstreamSha)}\``,
    `- Target: \`${report.brandRef}\` @ \`${shortSha(report.brandSha)}\``,
    report.mergedSha ? `- Sync commit: \`${shortSha(report.mergedSha)}\`` : null,
    `- Checks: \`${checks}\``,
    `- Labels: ${labels.map((label) => `\`${label}\``).join(", ")}`,
    "",
    "## AI Review",
    "",
    review.summary ? review.summary : "No reviewer summary was produced.",
    risks.length > 0 ? "## Risks / follow-up" : null,
    risks.length > 0 ? risks.map((risk) => `- ${risk}`).join("\n") : null,
    conflictFiles.length > 0 ? "## Conflict files" : null,
    conflictFiles.length > 0 ? conflictFiles.map((file) => `- \`${file}\``).join("\n") : null,
    upstreamCommits ? ["## Upstream Commits", "```text", upstreamCommits, "```"].join("\n") : null,
    diffStat ? ["## Diff Stat", "```text", diffStat, "```"].join("\n") : null,
    nameStatus ? ["## Files", "```text", nameStatus, "```"].join("\n") : null,
    report.checks?.ok === false && report.checks.output
      ? ["## Check Output", "```text", truncate(report.checks.output, 12000), "```"].join("\n")
      : null,
    "",
  ]
    .filter((line) => line !== null)
    .join("\n");
}

function firstLines(value, maxLines) {
  if (typeof value !== "string") return "";
  const lines = value.split("\n").filter(Boolean);
  const selected = lines.slice(0, maxLines).join("\n");
  if (lines.length <= maxLines) return selected;
  return `${selected}\n... truncated ${lines.length - maxLines} lines ...`;
}

function shortSha(value) {
  return typeof value === "string" && value.length >= 12 ? value.slice(0, 12) : value;
}

function createPullRequest({ cwd, syncBranch, prBodyPath, report, labels }) {
  const gh = spawnSync("gh", ["--version"], { encoding: "utf8" });
  if (gh.status !== 0) {
    throw new Error("GitHub CLI is not available; cannot create upstream sync PR.");
  }

  const labelsReady = ensureManagedLabels(cwd);
  const titlePrefix = report.status === "auto-merge-ready" ? "Auto-sync" : "Review upstream sync";
  const title = `${titlePrefix}: T3 Code into TritonGPT (${report.upstreamSha.slice(0, 12)})`;
  const labelArgs = labelsReady ? labels.flatMap((label) => ["--label", label]) : [];
  const repoArgs = githubRepoArgs();
  const result = spawnSync(
    "gh",
    [
      "pr",
      "create",
      "--base",
      config.brandBranch,
      "--head",
      syncBranch,
      "--title",
      title,
      "--body-file",
      prBodyPath,
      ...labelArgs,
      ...repoArgs,
    ],
    {
      cwd,
      encoding: "utf8",
      maxBuffer: 20 * 1024 * 1024,
    },
  );

  if (result.status !== 0) {
    throw new Error(
      `gh pr create failed.\n${truncate(`${result.stdout || ""}\n${result.stderr || ""}`, 4000)}`,
    );
  }

  const output = `${result.stdout || ""}\n${result.stderr || ""}`.trim();
  const url = output.match(/https:\/\/github\.com\/\S+\/pull\/\d+/)?.[0] || null;
  console.log(output || `Created PR for ${syncBranch}.`);
  return { url, labels };
}

function ensureManagedLabels(cwd) {
  let ok = true;
  const repoArgs = githubRepoArgs();
  for (const label of managedLabels) {
    const result = spawnSync(
      "gh",
      [
        "label",
        "create",
        label.name,
        "--color",
        label.color,
        "--description",
        label.description,
        "--force",
        ...repoArgs,
      ],
      {
        cwd,
        encoding: "utf8",
        maxBuffer: 1024 * 1024,
      },
    );
    if (result.status !== 0) {
      ok = false;
      console.warn(
        `Could not ensure label '${label.name}'; creating PR without labels. ${truncate(
          `${result.stdout || ""}\n${result.stderr || ""}`,
          1000,
        )}`,
      );
    }
  }
  return ok;
}

function labelsForReport({ report, canAutoMerge }) {
  const labels = new Set(["automation:upstream-sync"]);
  if (canAutoMerge) {
    labels.add("automation:auto-merge-ready");
  } else {
    labels.add("needs-human-review");
  }

  if (String(report.reason || "").includes("conflict") || report.conflicts?.length > 0) {
    labels.add("upstream-conflict");
  }

  if (report.checks?.ok === false) {
    labels.add("checks-failed");
  }

  if (report.review?.reviewer === "agent" || report.review?.phase || report.review?.agentChange) {
    labels.add("agent-attempted");
  }

  if (report.review && !canAutoMerge) labels.add("ai-review-risk");

  return [...labels];
}

function mergePullRequest({ cwd, syncBranch, report, pr }) {
  const mergeFlag =
    config.prMergeMethod === "squash"
      ? "--squash"
      : config.prMergeMethod === "rebase"
        ? "--rebase"
        : "--merge";
  const target = pr.url || syncBranch;
  const args = ["pr", "merge", target, mergeFlag, "--delete-branch", ...githubRepoArgs()];
  if (config.prMergeMode === "auto") args.push("--auto");
  if (report.mergedSha) args.push("--match-head-commit", report.mergedSha);

  const result = spawnSync("gh", args, {
    cwd,
    encoding: "utf8",
    maxBuffer: 20 * 1024 * 1024,
  });

  if (result.status !== 0) {
    throw new Error(
      `gh pr merge failed.\n${truncate(`${result.stdout || ""}\n${result.stderr || ""}`, 4000)}`,
    );
  }

  console.log(
    `${result.stdout || ""}${result.stderr || ""}`.trim() || `Merged PR for ${syncBranch}.`,
  );
}

function githubRepoArgs() {
  return config.githubRepo ? ["--repo", config.githubRepo] : [];
}

function runGit(args, opts = {}) {
  const result = spawnGit(args, opts);
  if (result.status !== 0 && !opts.allowFailure) {
    throw new Error(`git ${args.join(" ")} failed with exit code ${result.status}.`);
  }
  return result;
}

function spawnGit(args, opts = {}) {
  return spawnSync("git", args, {
    cwd: opts.cwd || repoRoot,
    encoding: "utf8",
    stdio: opts.stdio || ["ignore", "pipe", "pipe"],
  });
}

function gitStdout(args, opts = {}) {
  try {
    return execFileSync("git", args, {
      cwd: opts.cwd || repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", opts.allowFailure ? "pipe" : "inherit"],
      maxBuffer: 20 * 1024 * 1024,
    }).trim();
  } catch (error) {
    if (opts.allowFailure) return "";
    throw error;
  }
}

function truncate(value, maxLength) {
  if (value.length <= maxLength) return value;
  return `${value.slice(0, maxLength)}\n... truncated ${value.length - maxLength} chars ...`;
}

